// Add

function addTwo(a,b){
    return a+b
}

console.log(addTwo(1,2))

//Subtract

function subtractTwo(x,y){
    return x-y;
}
console.log(subtractTwo(4, 2))

//Multiply

function multiplyTwo(p, q){
    return p*q
}
console.log(multiplyTwo(4, 3))

//Divide
function divideTwo(r, s){
    return r/s
}
console.log(divideTwo(4, 2))

//Modulus or remainder
function modulusTwo(t, u){
    return t%u
}
console.log(modulusTwo(5, 2))
